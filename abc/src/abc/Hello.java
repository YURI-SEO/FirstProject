package abc;
/**
 * 이 수업은 자바 수업입니다.
 * @author 서유리
 *
 * 두수의 합을 계싼하는 애플리케이션
 */
public class Hello {

	public static void main(String[] args) {
		int x;			// 첫 번째 정수를 저장할 변수
		int y;			// 두 번째 정수를 저장할 변수
		int sum;		// 두 정수의 합을 저장하는 변수
		
		x = 100;
		y = 200;
		sum = x + y;
		
		System.out.println(sum);
		return;		
		
	}

}
